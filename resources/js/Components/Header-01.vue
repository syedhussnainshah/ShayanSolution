<script>
import Swal from 'sweetalert2';
import axios from 'axios';
export default {
    data() {
        return {
            formData: {
                name: "",
                email: "",
                phone: "",
                projectDetails: "",
            },
            CountedNameCharacters: 0,
            formDataErorrs: {
                phone: '',
                name: '',
                email: '',
                projectDetails: '',
            },
        };
    },

    computed: {
        isFormIncomplete() {
            return (
                this.formData.name === "" ||
                this.formData.email === "" ||
                this.formData.phone === "" ||
                this.formData.projectDetails === ""
            );
        },
    },
    methods: {
        CountNameCharacters() {
            this.CountedNameCharacters = this.formData.name.length;

        },
        clearPhoneError() {
            this.formDataErorrs.phone = '';
        },
        clearEmailError() {
            this.formDataErorrs.email = '';
        },
        clearNameError() {
            this.formDataErorrs.name = '';
        },
        clearProjectDetailsError() {
            this.formDataErorrs.projectDetails = '';
        },

        submitForm() {
            this.formDataErorrs = {
                phone: '',
                name: '',
                email: '',
                projectDetails: '',
            };
            console.log(this.formData);
            axios.post('/api/headerformstore', this.formData)
                .then((response) => {
                    console.log(response);
                    this.formData = {
                        name: "",
                        email: "",
                        phone: "",
                        projectDetails: "",
                    };
                    this.CountedNameCharacters = 0;
                    Swal.fire({
                        // title: 'Hello!',
                        text: response.data.message,
                        icon: 'success',
                    });
                })
                .catch((error) => {
                    // Swal.fire({
                    //     icon: 'error',
                    //     title: 'Oops...',
                    //     text: error.response.data.message,
                    // })
                    if (error.response.data.errors.name) {
                        this.formDataErorrs.name = error.response.data.errors.name[0];
                    }
                    if (error.response.data.errors.email) {
                        this.formDataErorrs.email = error.response.data.errors.email[0];
                    }
                    if (error.response.data.errors.phone) {
                        this.formDataErorrs.phone = error.response.data.errors.phone[0];
                    }
                    if (error.response.data.errors.projectDetails) {
                        this.formDataErorrs.projectDetails = error.response.data.errors.projectDetails[0];
                    }



                    // error.response.data.errors.phone ? this.error.phone = error.response.data.errors.phone[0] : this.error.phone = '';
                });
        },
    },
};
</script>
